					<br><br>Mgahinga’s most striking features are its three conical, extinct volcanoes, part of the spectacular Virunga Range that lies along the border region of Uganda, Congo and Rwanda. Mgahinga forms part of the much larger Virunga Conservation Area which includes adjacent parks in these countries. The volcanoes’ slopes contain various ecosystems and are biologically diverse, and their peaks provide a striking backdrop to this gorgeous scenery. 
